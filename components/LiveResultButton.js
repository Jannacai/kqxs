import { useState, useEffect } from "react";
import React from 'react';
import LiveResultModal from './LiveResultModal';
import styles from '../styles/LiveResultButton.module.css';

const LiveResultButton = ({
    station = 'xsmt',
    isLiveWindow = true,
    buttonText = "Xem X·ªï s·ªë Tr·ª±c ti·∫øp",
    buttonStyle = "primary",
    size = "medium",
    isForum = false,
    position = "bottom-left"
}) => {
    const [isModalOpen, setIsModalOpen] = useState(false);
    const [shouldShow, setShouldShow] = useState(false);

    // Ki·ªÉm tra th·ªùi gian hi·ªÉn th·ªã n√∫t
    useEffect(() => {
        const checkTimeAndShow = () => {
            const now = new Date();
            const currentHour = now.getHours();
            
            // XSMN hi·ªÉn th·ªã t·ª´ 16h-16h59
            if (station === 'xsmn') {
                setShouldShow(currentHour === 16);
            }
            // XSMT hi·ªÉn th·ªã t·ª´ 17h-17h59
            else if (station === 'xsmt') {
                setShouldShow(currentHour === 17);
            }
            // C√°c tr∆∞·ªùng h·ª£p kh√°c kh√¥ng hi·ªÉn th·ªã
            else {
                setShouldShow(false);
            }
        };

        // Ki·ªÉm tra ngay l·∫≠p t·ª©c
        checkTimeAndShow();

        // C·∫≠p nh·∫≠t m·ªói ph√∫t ƒë·ªÉ ƒë·∫£m b·∫£o ch√≠nh x√°c
        const interval = setInterval(checkTimeAndShow, 60000);

        return () => clearInterval(interval);
    }, [station]);

    // Debug: Log th·ªùi gian hi·ªán t·∫°i v√† tr·∫°ng th√°i hi·ªÉn th·ªã (ch·ªâ trong development)
    useEffect(() => {
        if (process.env.NODE_ENV === 'development') {
            const now = new Date();
            console.log(`[LiveResultButton ${station}] Current time: ${now.toLocaleTimeString()}, Hour: ${now.getHours()}, Should show: ${shouldShow}`);
        }
    }, [shouldShow, station]);

    const handleToggleModal = () => {
        const newState = !isModalOpen;
        setIsModalOpen(newState);

        // Track button click for analytics
        if (typeof gtag !== 'undefined') {
            gtag('event', newState ? 'open_live_result_modal' : 'close_live_result_modal', {
                event_category: 'user_interaction',
                event_label: `live_result_button_${station}`
            });
        }
    };

    const handleCloseModal = () => {
        setIsModalOpen(false);
    };

    const getButtonClassName = () => {
        const baseClass = styles.liveResultButton;
        const styleClass = styles[buttonStyle] || styles.primary;
        const sizeClass = styles[size] || styles.medium;
        const forumClass = isForum ? styles.forumButton : '';
        const forumStyleClass = isForum ? styles[buttonStyle] || styles.primary : '';
        const positionClass = isForum ? styles[position] || styles.bottomLeft : '';

        return `${baseClass} ${styleClass} ${sizeClass} ${forumClass} ${forumStyleClass} ${positionClass}`;
    };

    // N·∫øu kh√¥ng n√™n hi·ªÉn th·ªã, return null
    if (!shouldShow) {
        return null;
    }

    return (
        <>
            <button
                className={getButtonClassName()}
                onClick={handleToggleModal}
                aria-label={isModalOpen ? `ƒê√≥ng xem x·ªï s·ªë ${station === 'xsmn' ? 'Mi·ªÅn Nam' : 'Mi·ªÅn Trung'} tr·ª±c ti·∫øp` : `M·ªü xem x·ªï s·ªë ${station === 'xsmn' ? 'Mi·ªÅn Nam' : 'Mi·ªÅn Trung'} tr·ª±c ti·∫øp`}
                title={isModalOpen ? `ƒê√≥ng k·∫øt qu·∫£ x·ªï s·ªë ${station === 'xsmn' ? 'Mi·ªÅn Nam' : 'Mi·ªÅn Trung'} tr·ª±c ti·∫øp` : `Xem k·∫øt qu·∫£ x·ªï s·ªë ${station === 'xsmn' ? 'Mi·ªÅn Nam' : 'Mi·ªÅn Trung'} tr·ª±c ti·∫øp`}
            >
                <span className={styles.buttonIcon}>{isModalOpen ? '‚úï' : (station === 'xsmn' ? 'üé≤' : 'üéØ')}</span>
                <span className={styles.buttonText}>
                    {isModalOpen ? `ƒê√≥ng ${station.toUpperCase()} Live` : buttonText}
                </span>
                {!isModalOpen && (
                    <span className={styles.liveIndicator}>
                        <span className={styles.pulse}></span>
                        LIVE
                    </span>
                )}
            </button>

            <LiveResultModal
                isOpen={isModalOpen}
                onClose={handleCloseModal}
                station={station}
                isLiveWindow={isLiveWindow}
                isForum={isForum}
            />
        </>
    );
};

export default React.memo(LiveResultButton); 